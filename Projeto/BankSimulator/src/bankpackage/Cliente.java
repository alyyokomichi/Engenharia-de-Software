package bankpackage;

public class Cliente {

		private int saldo;
		private int credito;
		
		public void setSaldo(int saldo){
			this.saldo = saldo;		
		}
		public int getSaldo(){
			return this.saldo;
		}
		public void setCredito(int credito){
			this.credito = credito;
		}
		public int getCredito(){
			return this.credito;
		}

}
